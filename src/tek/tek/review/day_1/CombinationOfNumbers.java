package tek.review.day_1;

public class CombinationOfNumbers {

    public static void main(String[] args) {

        // Outer Loop
        for (int i = 1; i <= 5; i++) {
            // Inner Loop
            for ( int j = 1; j <= 5; j++ ) {
                System.out.println(i + " " + j);
            }

        }

    }

}
