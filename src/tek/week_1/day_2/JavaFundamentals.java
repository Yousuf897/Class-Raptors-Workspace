package tek.week_1.day_2;

public class JavaFundamentals {
/*
    Understanding the fundamentals of Java is like learning the basic rules and moves in a game of
    chess. Once you get these down, you'll be equipped to dive into more complex topics.
    Here are some Java fundamentals explained in simple terms:
*/

/*
    1. Variables: Think of variables like containers in your kitchen where you store things like sugar,
     salt, and spices. In Java, variables hold data that you can use later.

    2. Data Types: Just as you wouldn't store soup in a salt shaker, each variable in Java has a
    specific "type" that tells us what kind of data it can hold. Common types include int for integers,
     double for floating-point numbers, and String for text.

    3. Operators: Operators are like the basic calculations you can do on a calculator: addition, subtraction,
       etc. In Java, you have various operators like +, -, *, and / to perform operations.

   4. Conditionals: Imagine a traffic light. If it's green, you go; if it's red, you stop.
   Conditionals (if, else, else if) in Java work in a similar way;
   they let your program make decisions based on conditions that you are doing to provide.

   5. Loops: Loops are like treadmills: they let you do something repeatedly
    without having to write the same code over and over.

   6. Arrays: Think of an array like a row of mailboxes. Each mailbox can hold a piece of data,
      and you can access it using its position (index).

  7. Methods: Methods are like little machines in a factory. You give them some material (arguments),
  they do something to it, and then they might give something back (return a value).

  8. Classes and Objects: A class is like a blueprint for building a house.
  An object is the actual house built from that blueprint.
  Classes encapsulate attributes (variables) and behaviors (methods) that their objects will have.

  These are just the building blocks, but once you understand these fundamentals,
  you can start building more complex applications,
  just like how knowing the basic rules of chess allows you to play complex games.

*/

}
